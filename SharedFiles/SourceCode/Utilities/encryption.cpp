#include	"encryption.hpp"



namespace	DawnEngine
{

	namespace	Utility
	{

		//	Function responsible of encrypting the given string.
		std::string	encrypt( const std::string& input , const std::string& salt , const unsigned int passes )
		{
			salt;passes;
			return input;
		};

		//	Function responsible of decrypting the given string.
		std::string	decrypt( const std::string& input , const std::string& salt , const unsigned int passes )
		{
			salt;passes;
			return input;
		};

	}	/* Utilities */

}	/* DawnEngine */